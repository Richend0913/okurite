FALLBACK = "カタログ"

def alive(u):
    try:
        req = urllib.request.Request(u, headers={"User-Agent": "Mozilla/5.0"})
        return urllib.request.urlopen(req, timeout=10).status == 200
    except Exception:
        return False

def build_pool():
    g = json.load(open(GIFTS, encoding="utf-8"))
    bycat = collections.defaultdict(list)
    for x in g:
        u = x.get("img", "")
        if u.startswith("http"):
            bycat[x.get("category", "?")].append(u)
    # 生存チェック(並列)
    allurls = sorted({u for v in bycat.values() for u in v})
    with ThreadPoolExecutor(max_workers=16) as ex:
        live = dict(zip(allurls, ex.map(alive, allurls)))
    pool = {c: [u for u in us if live.get(u)] for c, us in bycat.items()}
    pool = {c: us for c, us in pool.items() if us}
    return pool

def genre(title):
    for kws, cat in RULES:
        for k in kws:
            if k in title:
                return cat
    return None

def pick(pool, cat, title):
    urls = pool.get(cat) or pool.get(FALLBACK) or next(iter(pool.values()))
    h = int(hashlib.md5(title.encode()).hexdigest(), 16)
    return urls[h % len(urls)]

def main():
    apply = "--apply" in sys.argv
    pool = build_pool()
    print("生存画像プール:", {c: len(v) for c, v in sorted(pool.items())})
    stats = collections.Counter(); unmatched = []
    total = 0
    for f in glob.glob(ROOT + r"\blog\*.html"):
        h = open(f, encoding="utf-8").read()
        # gift-card-title と直前の gift-card-img をペアで処理: imgタグ単位で、その後の最初のtitleを使う
        cards = list(re.finditer(r'(<img class="gift-card-img"[^>]*?src=")([^"]+)(")', h))
        # 各imgの後方にあるtitleを対応付け
        titles = [(m.start(), re.sub("<.*?>", "", m.group(1)).strip())
                  for m in re.finditer(r'gift-card-title">(.*?)</div>', h, re.S)]
        def title_after(pos):
            cand = [t for p, t in titles if p > pos]
            return cand[0] if cand else ""
        new = h; off = 0
        for m in cards:
            title = title_after(m.start())
            title = re.sub(r"^\d+\.\s*", "", title)
            cat = genre(title)
            stats[cat or "(未判定)"] += 1
            if not cat:
                unmatched.append(title[:30])
            url = pick(pool, cat or FALLBACK, title)
            s, e = m.start(2) + off, m.end(2) + off
            new = new[:s] + url + new[e:]
            off += len(url) - (m.end(2) - m.start(2))
            total += 1
        if apply and new != h:
            open(f, "w", encoding="utf-8").write(new)
    print(f"\n処理カード: {total} / 記事: {len(glob.glob(ROOT + chr(92)+'blog'+chr(92)+'*.html'))}")
    print("ジャンル判定分布:", dict(stats.most_common()))
    print("未判定サンプル:", unmatched[:20], f"... 計{len(unmatched)}件" if unmatched else "")
    print("APPLY済み" if apply else "(dry-run。--apply で書き込み)")

if __name__ == "__main__":
    main()
